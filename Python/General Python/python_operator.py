a = int(input("Enter a number: "))
b = int(input("Enter another number: "))
print("\nThe sum of the two numbers is", a+b)
print("\nThe difference of the two numbers is", a-b)
print("\nThe product of the two numbers is", a*b)
print("\nThe quotient of the two numbers is", a/b)
print("\nThe remainder of the two numbers is", a%b)# Modulus
print("\nThe result of a to the power b is", a**b)# Exponentiation